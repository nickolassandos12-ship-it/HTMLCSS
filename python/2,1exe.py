# funções basicas de calculo 
def inicio():
    print("calcule")

    num1 = float(input("digite o primeiro número para efetuar uma multiplicação \n"))
    num2 = float(input("digite o segundo número \n"))

    multipicacao = num1 * num2
    divisao = num1 / num2  
    adicao = num1 + num2
    subtracao = num1 - num2
    print(f"o resultado da multiplicação é: {multipicacao}")
    print(f"o resultado da divisão é: {divisao}")
    print(f"o resultado da adição é: {adicao}")
    print(f"o resultado da subtração é: {subtracao}")

    
    

inicio()